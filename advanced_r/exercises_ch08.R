# 2. Predict the results of evaluating the following code

show_condition <- function(code) {
  tryCatch(
    error = function(cnd) "error",
    warning = function(cnd) "warning",
    message = function(cnd) "message",
    {
      code
      NULL
    }
  )
}

show_condition(stop("!"))
# "error"
show_condition(10)
# NULL
show_condition(warning("?!"))
# "warning"
show_condition({
  10
  message("?")
  warning("?!")
})
# "message"

